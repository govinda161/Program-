# Initialize a variable to hold the sum
total_sum = 0  # Start with a sum of 0

# Loop through numbers from 1 to 10
for number in range(1, 11):  # range(1, 11) includes numbers from 1 to 10
    # Add the current number to the total sum
    total_sum += number  # Increment total_sum by the current number

# Print the final sum
print("The sum of numbers from 1 to 10 is:", total_sum)  # Output the result
