# Find the three greatest numbers
a=1
b=2
c=3
if a>=b and a>=c:    # If a is greater than b and a is greater than c, then a is greatest among other values
    print("A is greatest amongs other values",a)
elif b>=a and b>=c:    # If b is greater than a and b is greater than c, then b is greatest among other values
    print("B is greatest among other values",b)
else:    # then c is greatest among other values
    print("C is greatest among other values",c)
